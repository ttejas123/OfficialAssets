import React, { useEffect, useState } from "react";
import DataJSON from "./Data.json";
import "./styles.css";

import { Line } from "react-chartjs-2";

export default function App() {
  const [datasetSize, setDatasetSize] = useState(30);
  const [start, setStart] = useState(0);
  const [end, setend] = useState(datasetSize);
  const [label, setLabel] = useState([]);
  const [Balence, setBalence] = useState([]);
  const [Debited, setDebited] = useState([]);
  const [credited, setcredited] = useState([]);
  const [creditedTotal, setCreditedTotal] = useState([]);
  const checkDataInRange = (index) => {
    return start < index && end >= index;
  };

  const processData = () => {
    const l = [];
    const bal = [];
    const cr = [];
    const crTotal = [];
    const debit = [];
    const limit = 500000;

    const followLimit = (val) => {
      return val > limit ? 0 : val;
    };
    DataJSON.Data.map((val, index) => {
      if (checkDataInRange(index)) {
        l.push(val.Date);
        bal.push(val.Balence);
        cr.push(followLimit(val.credited));
        debit.push(followLimit(val.Debited));
        crTotal.push(
          crTotal.length > 0
            ? crTotal[crTotal.length - 1] + val.credited
            : val.credited
        );
      }
    });

    setLabel(l);
    setBalence(bal);
    setDebited(debit);
    setcredited(cr);
    setCreditedTotal(crTotal);
  };

  useEffect(() => {
    processData();
  }, [start, datasetSize]);
  const data = {
    labels: label,
    datasets: [
      {
        label: "Total Chnage in Account Balence",
        data: Balence,
        fill: true,
        backgroundColor: "rgba(75,192,192,0.2)",
        borderColor: "rgba(75,192,192,1)"
      },
      {
        label: "Credited",
        data: credited,
        fill: true,
        borderColor: "#000fff"
      },
      {
        label: "Debited",
        data: Debited,
        fill: true,
        borderColor: "#fff000"
      },
      {
        label: "Credited_Total",
        data: creditedTotal,
        fill: true,
        borderColor: "#ffgg000"
      }
    ]
  };
  return (
    <div className="App">
      <Line data={data} />

      <button
        onClick={() => {
          setStart((pre) => pre - datasetSize);
          setend((pre) => pre - datasetSize);
        }}
      >{`<-`}</button>
      <button
        onClick={() => {
          setStart((pre) => pre + datasetSize);
          setend((pre) => pre + datasetSize);
        }}
      >{`->`}</button>
      <br />
      {datasetSize}
      <br />
      <button
        onClick={() => {
          setStart(0);
          setend(0 + datasetSize - 5);
          setDatasetSize((pre) => pre - 5);
        }}
      >
        -5
      </button>
      <button
        onClick={() => {
          setStart(0);
          setend(0 + datasetSize - 1);
          setDatasetSize((pre) => --pre);
        }}
      >
        -1
      </button>
      <button
        onClick={() => {
          setStart(0);
          setend(0 + datasetSize + 1);
          setDatasetSize((pre) => ++pre);
        }}
      >
        +1
      </button>
      <button
        onClick={() => {
          setStart(0);
          setend(0 + datasetSize + 5);
          setDatasetSize((pre) => pre + 5);
        }}
      >
        +5
      </button>
    </div>
  );
}
